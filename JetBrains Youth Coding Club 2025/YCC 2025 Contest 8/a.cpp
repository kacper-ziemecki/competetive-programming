void solve(){
  int gud,bad=0,b,res=0;
  cin >> gud >> b;
  while(gud){
    res += gud;
    int old_gud = gud;
    gud = (bad+gud)/b;
    bad = (old_gud+bad) - gud*b;
  }
  cout << res << endl;
}

