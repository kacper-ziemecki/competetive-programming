void solve(){
  ll n;
  cin >> n;
  ll res = n/7 *7;
  ll cnt = n%7;
  while(cnt%4 != 0 && res > 0){
    cnt += 7;
    res -= 7;
  }
  cout << (res < 0 || cnt%4 != 0 ? -1 : res) << endl;
} 
 
